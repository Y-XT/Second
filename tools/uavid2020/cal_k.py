# 解析数据：格式为 ID, MODEL, width, height, fx, cx, cy, distortion
lines = raw_data.strip().split("\n")
rows = []
for line in lines:
    parts = line.split()
    width = int(parts[2])
    height = int(parts[3])
    fx = float(parts[4])
    cx = float(parts[5])
    cy = float(parts[6])

    # 对 4096 宽度图像做中心裁剪（左移主点）
    if width == 4096:
        offset = (4096 - 3840) // 2
        width = 3840  # 视为裁剪后图像
        cx = cx - offset  # 主点左移

    # 归一化
    fx_norm = fx / width
    fy_norm = fx / height  # SIMPLE_RADIAL 假设 fx=fy
    cx_norm = cx / width
    cy_norm = cy / height

    rows.append([fx_norm, fy_norm, cx_norm, cy_norm])

# 转为 DataFrame，计算平均值
df = pd.DataFrame(rows, columns=["fx_norm", "fy_norm", "cx_norm", "cy_norm"])
